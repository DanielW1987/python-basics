# print with double quotes
print("Hello, World!")

# Print with single quotes
print('Hello, World!')

# Print with String multiplication
print("=" * 20)

# Print with more than one argument
print("Hello", ",", "World", "!")

# Print with formatting
a = 10
b = 5
print("The result of {} + {} = {}".format(a, b, a + b))
print("The result of {2} + {1} = {0}".format(a + b, b, a))
print("The result of %d + %d = %.2f" % (a, b, a + b))

# Print with separator
print("Col1", "Col2", "Col3", sep=" | ")
