# integers
a = 10
intValue: int = 10

print(type(a), type(intValue))


# floats
b = 3.141
floatValue: float = 3.141

print(type(b), type(floatValue))


# complex
c = 3 + 5j
print(type(c))


# binary: starts with 0B
e = 0B1010
print(type(e))
print(e)


# hexadecimal: starts with 0X
f = 0XFF
print(type(f))
print(f)


# octal: start with 0o
o = 0o530
print(type(o))
print(o)
